"""Chinese / East-Asian flavored CTF ciphers.

Includes:
- 与佛论禅 / 新约佛论禅 (with basic decoding support)
- 社会主义核心价值观 (12 values → digits 0-11 in groups of 2 → ascii)
- 当铺密码 (Chinese pawnshop characters → digit by stroke count)
- 中文电码 / Chinese commercial telegraph code (4-digit -> CJK, common chars table)
- 反切码 etc detection placeholders
"""
from __future__ import annotations

import re

from cipherid.cipher import Candidate, Cipher

# ----------------------------------------------------------------
# 社会主义核心价值观
# ----------------------------------------------------------------
CORE_VALUES = [
    "富强", "民主", "文明", "和谐",
    "自由", "平等", "公正", "法治",
    "爱国", "敬业", "诚信", "友善",
]
_value_index = {v: i for i, v in enumerate(CORE_VALUES)}


class CoreValuesCipher(Cipher):
    """社会主义核心价值观编码。

    每个字符两个核心价值观词组合，对应一位 base27 数字。
    一个完整字符: 用 (value1, value2) 表示，索引: 12*v1 + v2 (12 进制)
    然后把字节流按可见 ASCII 输出。
    实际实现可能略有差异，多种变种存在 — 这里实现最常见 1 byte = 2 词版。
    """
    id = "core_values"
    name = "社会主义核心价值观编码"
    category = "chinese"

    def identify(self, text: str) -> list[Candidate]:
        words = self._tokenize(text)
        if not words:
            return []
        if not all(w in _value_index for w in words):
            return []
        plain = self.decode(text)
        if plain is None:
            return []
        return [Candidate(self.id, self.name, self.category, 0.95, decoded=plain,
                          notes="core-values encoding, 2 values per character")]

    def decode(self, text: str, key: str | None = None) -> str | None:
        words = self._tokenize(text)
        if not words or len(words) % 2 != 0:
            return None
        out_chars = []
        for i in range(0, len(words), 2):
            a = _value_index.get(words[i])
            b = _value_index.get(words[i+1])
            if a is None or b is None:
                return None
            n = a * 12 + b
            if not (32 <= n <= 126):
                return None
            out_chars.append(chr(n))
        return "".join(out_chars)

    def encode(self, text: str, key: str | None = None) -> str | None:
        result = []
        for b in text.encode("utf-8"):
            if b < 32 or b > 126:
                return None
            a = b // 12
            b_val = b % 12
            if a >= len(CORE_VALUES) or b_val >= len(CORE_VALUES):
                return None
            result.append(CORE_VALUES[a] + CORE_VALUES[b_val])
        return "".join(result)

    @staticmethod
    def _tokenize(text: str) -> list[str]:
        # Strip whitespace, then take consecutive 2-char windows that are in CORE_VALUES
        t = re.sub(r"\s+", "", text)
        words = []
        i = 0
        while i < len(t) - 1:
            two = t[i:i+2]
            if two in _value_index:
                words.append(two)
                i += 2
            else:
                return []
        return words


