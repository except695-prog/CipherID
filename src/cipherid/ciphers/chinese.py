class PinyinCipher(Cipher):
    """拼音密码: Chinese Pinyin encoding."""
    id = "pinyin"
    name = "拼音密码 (Pinyin)"
    category = "chinese"

    def identify(self, text: str) -> list[Candidate]:
        t = text.strip()
        # Pinyin typically looks like sequences of lowercase letters with possible numbers
        if not re.fullmatch(r"[a-z1-5\s]+", t.lower()):
            return []
        # Check if it looks like pinyin syllables
        parts = t.lower().split()
        if len(parts) < 3:
            return []
        # Basic pinyin syllable validation
        valid_initials = set("bpmfdtnlgkhjqxczcsrzwyeiou")
        valid_finals = {"a", "o", "e", "i", "u", "v", "ai", "ei", "ao", "ou",
                        "an", "en", "ang", "eng", "ong", "ia", "ie", "iao",
                        "iu", "ian", "in", "iang", "ing", "ua", "uo", "uai",
                        "ui", "uan", "un", "uang", "ue", "van", "vn"}
        looks_pinyin = all(
            len(p) >= 1 and p[0] in valid_initials and
            any(p.endswith(f) for f in valid_finals)
            for p in parts
        )
        if looks_pinyin:
            return [Candidate(self.id, self.name, self.category, 0.5,
                              decoded=None,
                              notes="Chinese pinyin — convert to hanzi with lookup table")]
        return []


