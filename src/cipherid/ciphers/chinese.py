class KeyboardCipher(Cipher):
    id = "keyboard"
    name = "键盘密码 (QWERTY 偏移)"
    category = "chinese"

    def identify(self, text: str) -> list[Candidate]:
        if not re.fullmatch(r"[A-Za-z\s]+", text):
            return []
        rows = ["qwertyuiop", "asdfghjkl", "zxcvbnm"]
        shift_map = {}
        for row in rows:
            for i, c in enumerate(row[:-1]):
                shift_map[row[i+1]] = c
        out = "".join(shift_map.get(c.lower(), c) for c in text)
        if out != text and any(c.isalpha() for c in out):
            return [Candidate(self.id, "QWERTY shift-left", self.category, 0.4,
                              decoded=out)]
        return []

    def decode(self, text: str, key: str | None = None) -> str | None:
        rows = ["qwertyuiop", "asdfghjkl", "zxcvbnm"]
        shift_map = {}
        for row in rows:
            for i, c in enumerate(row[:-1]):
                shift_map[row[i+1]] = c
        out = "".join(shift_map.get(c.lower(), c) for c in text)
        return out if out != text else None

    def encode(self, text: str, key: str | None = None) -> str | None:
        rows = ["qwertyuiop", "asdfghjkl", "zxcvbnm"]
        shift_map = {}
        for row in rows:
            for i, c in enumerate(row[:-1]):
                shift_map[c] = row[i+1]
        out = "".join(shift_map.get(c.lower(), c) for c in text)
        return out if out != text else None


class WuxingCipher(Cipher):
    """五行密码: Five Elements cipher — maps Chinese characters to 金木水火土."""
    id = "wuxing"
    name = "五行密码 (Five Elements)"
    category = "chinese"

    WUXING_MAP = {
        "金": "J", "木": "M", "水": "S", "火": "H", "土": "T",
    }

    def identify(self, text: str) -> list[Candidate]:
        t = re.sub(r"\s+", "", text)
        if not t:
            return []
        if all(c in self.WUXING_MAP for c in t) and len(t) >= 2:
            decoded = self.decode(t)
            if decoded:
                return [Candidate(self.id, self.name, self.category, 0.75, decoded=decoded)]
        return []

    def decode(self, text: str, key: str | None = None) -> str | None:
        t = re.sub(r"\s+", "", text)
        return "".join(self.WUXING_MAP.get(c, "") for c in t) or None

    def encode(self, text: str, key: str | None = None) -> str | None:
        _rev = {v: k for k, v in self.WUXING_MAP.items()}
        result = []
        for c in text.upper():
            if c in _rev:
                result.append(_rev[c])
            else:
                return None
        return "".join(result)


