class ChineseTelegraphCipher(Cipher):
    id = "ctc"
    name = "中文电码 (CTC)"
    category = "chinese"

    def identify(self, text: str) -> list[Candidate]:
        parts = re.split(r"[\s,]+", text.strip())
        if len(parts) < 2 or any(len(p) != 4 or not p.isdigit() for p in parts):
            return []
        decoded = self.decode(text)
        conf = 0.85 if decoded else 0.6
        notes = "" if decoded else "partial table — may not decode all characters"
        return [Candidate(self.id, self.name, self.category, conf,
                          decoded=decoded, notes=notes)]

    def decode(self, text: str, key: str | None = None) -> str | None:
        parts = re.split(r"[\s,]+", text.strip())
        result = []
        for p in parts:
            if len(p) != 4 or not p.isdigit():
                return None
            ch = CTC_TABLE.get(p)
            if ch is None:
                return None
            result.append(ch)
        return "".join(result)

    def encode(self, text: str, key: str | None = None) -> str | None:
        _rev = {v: k for k, v in CTC_TABLE.items()}
        result = []
        for c in text:
            code = _rev.get(c)
            if code is None:
                return None
            result.append(code)
        return " ".join(result)


