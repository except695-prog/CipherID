    id = "tiangan_dizhi"
    name = "天干地支密码"
    category = "chinese"

    TIANGAN = "甲乙丙丁戊己庚辛壬癸"
    DIZHI = "子丑寅卯辰巳午未申酉戌亥"

    def identify(self, text: str) -> list[Candidate]:
        t = re.sub(r"\s+", "", text)
        if not t:
            return []
        tg_count = sum(1 for c in t if c in self.TIANGAN)
        dz_count = sum(1 for c in t if c in self.DIZHI)
        if tg_count + dz_count == len(t) and len(t) >= 2:
            return [Candidate(self.id, self.name, self.category, 0.65,
                              decoded=None,
                              notes="Heavenly Stems + Earthly Branches — needs key table")]
        return []


